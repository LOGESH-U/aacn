                <p class="agenda_msg">The draft State Mental Health Rules combined with rules relating to functioning of District Mental Health Rules prepared by CEO may be modified on the lines suggested in the meeting and sent to the Law Department for vetting and thereafter send it to Government of India for approval before making the rules notified in the Gazette.Till then the rules framed by Central Government may be followed.(Action CEO SMHA and Z section) The rules relating to Rights of persons with mental illness notified by Central Government will be republished in the State Gazette (Action Z section)</p>
